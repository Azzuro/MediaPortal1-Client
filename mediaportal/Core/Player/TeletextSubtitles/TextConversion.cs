        //Log.Debug("" + (teletext[i] & 0x7f));


        //int charIndex = (teletext[i] & 0x7f) - 0x20;
        ////assert(charIndex >= 0, "Convert: About to index position [" +lang + ", " + charIndex + "] source pos is " + i+ "( line " + (i % 25) + ")");

