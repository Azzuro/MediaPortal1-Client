        //    continue;
        //}
        //teletext[i] = vtx2iso8559_1_table[lang, charIndex];
        byte chr = teletext[i]; // input teletext character
        char chr2 = ' '; // output unicode character


        int txtLanguage;
        switch (languageCode)
        {
          case 0:
            txtLanguage = 1;
            break;
          case 1:
            txtLanguage = 4;
            break;
          case 2:
            txtLanguage = 11;
            break;
          case 3:
            txtLanguage = 5;
            break;
          case 4:
            txtLanguage = 3;
            break;
          case 5:
            txtLanguage = 8;
            break;
          case 6:
            txtLanguage = 0;
            break;
          default:
            txtLanguage = 1;
            break;
        }

        chr &= 0x7f; // strip parity bit

        switch (chr)
        {
            //case 0x00:
            //    //throw new Exception("Invalid character! 0x00");
            //    break;
          case 0x20:
            chr2 = ' ';
            break;
          case 0x23:
          case 0x24:
            chr2 = m_charTableA[txtLanguage, chr - 0x23];
            break;
          case 0x40:
            chr2 = m_charTableB[txtLanguage];
            break;
          case 0x5B:
          case 0x5C:
          case 0x5D:
          case 0x5E:
          case 0x5F:
          case 0x60:
            chr2 = m_charTableC[txtLanguage, chr - 0x5B];
            break;
          case 0x7B:
          case 0x7C:
          case 0x7D:
          case 0x7E:
            chr2 = m_charTableD[txtLanguage, chr - 0x7B];
            break;
            //case 0x7F:
            //    //throw new Exception("Invalid character! " + chr );
            //case 0xE0:
            //    //throw new Exception("Invalid character! " + chr);
            //case 0xE1:
            //    //throw new Exception("Invalid character! " + chr);
            //case 0xE2:
            //   // throw new Exception("Invalid character! " + chr);
            //case 0xE3:
            //  //  throw new Exception("Invalid character! " + chr);
